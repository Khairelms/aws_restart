# Defining list
myFruitList = ["apple", "banna", "cherry"]
print(myFruitList)
print(type(myFruitList))

# Accessing list by position
print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

# Change value in list
myFruitList[2] = "orange"
print(myFruitList)

# Defining Tuple
myFinalAnswerTuple = ("apple", "banna", "pineapple")
print(myFinalAnswerTuple)
print(type(myFinalAnswerTuple))

# Accessing a tuple by position
print(myFinalAnswerTuple[0])
print(myFinalAnswerTuple[1])
print(myFinalAnswerTuple[2])

# Defining a dictionary
myFavoriteFruitDictionary = {"Ali" : "apple",  "Samad" : "banana",  "Pablo" : "pineapple"}
print(myFavoriteFruitDictionary)
print(type(myFavoriteFruitDictionary))

# Accessing a dictionary by name
print(myFavoriteFruitDictionary["Ali"])
print(myFavoriteFruitDictionary["Samad"])
print(myFavoriteFruitDictionary["Pablo"])
